#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	int i;
	int size_;

	i = 0;
	size_ = (int)size;
	if (size == 0)
		return (ft_strlen(src));
	while (i < size_ - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
